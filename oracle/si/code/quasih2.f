  
      REAL*4 FUNCTION QUASIH2(te,w)

C-----CALCULATION OF THE ABSORPTION COEFFICIENT AQH2 FOR A GIVEN TEMPERATURE TE
C-----AND WAVELENGTH WLAM BY INTERPOLATION OF DATAS PROVIDED BY DOYLE, 1968.

      REAL*8 w
      REAL*4 te,WAVE(9),TEMP(7),AKQH2(7,9),wlam
      DATA WAVE/1540.,1750.,2000.,2222.,2500.,3000.,3640.,4500.,6667./
      DATA TEMP/2500.,3000.,4000.,5000.,6000.,8000.,10000./ 
      DATA AKQH2/
     *     57.848,57.568,57.245,57.075,56.985,56.932,56.933,
     *     59.921,59.277,58.474,58.010,57.722,57.405,57.258,
     *     62.686,61.627,60.309,59.517,59.003,58.391,58.071,
     *     64.900,63.503,61.754,60.707,60.021,59.194,58.736,
     *     67.383,65.589,63.350,62.017,61.138,60.066,59.461,
     *     70.998,68.612,65.644,63.871,62.696,61.257,60.428,
     *     74.516,71.558,67.874,65.672,64.218,62.430,61.391,
     *     78.143,74.636,70.258,67.629,65.886,63.732,62.473,
     *     83.810,79.548,74.180,70.933,68.767,66.068,64.482/

      QUASIH2 = 0.
      wlam = w*1.e8
      W0   = WAVE(1)
      NW1  = 1
      T0   = TEMP(1)
      NT1  = 1
      IF ( ABS(WLAM - W0) .LT. 0.05 ) GOTO 100
      CALL INDEX1(REAL(WLAM),WAVE,9,NW1,W0)
 100  CALL INDEX1(TE,TEMP,7,NT1,T0)
      NW2  = NW1 + 1 
      NT2  = NT1 + 1 
      DTE  = TE - TEMP(NT1)
      DLAM = ( WLAM - WAVE(NW1) ) / ( WAVE(NW2) - WAVE(NW1) )
      DIVTEMP = 1. / ( TEMP(NT2) - TEMP(NT1) )
      DAW  = ( AKQH2(NT2,NW1) - AKQH2(NT1,NW1) ) * DIVTEMP
      DAW1 = ( AKQH2(NT2,NW2) - AKQH2(NT1,NW2) ) * DIVTEMP
      AW   = AKQH2(NT1,NW1) + DAW * DTE
      AW1  = AKQH2(NT1,NW2) + DAW1 * DTE 
      AW   = AW + ( AW1 - AW ) * DLAM
      QUASIH2 = EXP(- AW)

      RETURN
      END 
